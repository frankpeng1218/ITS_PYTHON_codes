# 單元1 資料型態

#------------------------#
# 資料型態-為什麼資料要分型態?
print(2 + 3)
print("我" + "是" + "蟀哥")
print(5 * 9)
print("Frank"*3)
#----------------#
# 資料型態-資料分幾種型態？
print(10)
print(10.5)
print("你好")# 可以用"字串" or '字串' or '''字串''' or """字串"""
print(True)
#----------------#
# 資料型態-使用 type 檢查資料型態?
print(type(1))
print(type(1.5))
print(type(-9))

print(type(0.8))
print(type(-1.88))
print(type(1.0))

print(type('Frank'))

print(type(False))
print(type(20 > 10))
print(type(20 == 10))
#----------------#
# 資料型態-型態之間的轉換
a = '10'
b = '20'
c = int(a) * int(b)
print('相乘結果是:' + str(c))
