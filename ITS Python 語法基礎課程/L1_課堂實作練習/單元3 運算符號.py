# 單元3 運算符號

#------------------------#
# 運算符號-運算式組成-加號（＋）, 乘號（＊）
print(10 + 20 + 30)
print("Super" + "Idol" + '的笑容')
print(['I', 'Love', "You"] + ["My", "Friend"])

print(5 * 10)
print("美" * 10)
print(['6', '6', '6'] * 3)
#----------------#
# 運算符號-運算式組成-浮點數（.）
print(10 + 10)
print(10 - 10)
print(10 * 10)
print(10 / 10)# float

print(10 + 5.0)
print(10 - 5.0)
print(10.0 * 5)
print(10.0 / 5)
print(10 / 5)
#----------------#
# 運算符號-次方（**）
a = 2
b = 3
c = 5

print(a ** b)
print(a ** c)

print(b ** a)
print(b ** c)

print(c ** a)
print(c ** b)
#----------------#
# 運算符號-整除法（//）, 取餘數（%）
a = 10
b = 3
c = 10.0
print(int(a / b))
print(a / b)
print(a // b)
print(c // b)

print(100 % 3)
print(100 & 7)
print(100 % 10)
print(100 % 30)
print(100 % 70)
