# 程式碼縮排
num = int(input())
if num > 0:
    a = 10
    print(a)

    if num + a > 20:
        b = 5
        print(b)
    else:
        b = 10
        print(b)
elif num == 0:
    a = 0
    print(a)
else:
    a = 8
    print(a)
